public class Concatination{
	public static void main(String[] args){
		System.out.println("AB"+"CD"); // ABCD - String Concatination 
		System.out.println(10+20);//30 - Arithmatic add
		System.out.println(10+"CD");//10CD
		System.out.println("10"+20);//1020
		System.out.println("10"+20+10);//102010
		System.out.println(10 + 10 + "10"+30);//201030
		System.out.println("10"+(20+10));//1030
	}
}
